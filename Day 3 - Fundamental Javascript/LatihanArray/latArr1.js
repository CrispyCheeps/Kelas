let myArray = [];

// Tambahkan elemen 'apple' ke dalam array.
myArray.push("apple");
console.log(myArray);
// Tambahkan elemen 'banana' ke dalam array.
myArray.push("banana");
console.log(myArray);
// Hapus elemen terakhir dari array.
myArray.pop();
console.log(myArray);
// Tambahkan elemen 'orange' ke awal array.
myArray.unshift("orange");
console.log(myArray);
// Hapus elemen pertama dari array.
myArray.shift();
console.log(myArray);

